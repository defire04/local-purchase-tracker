let lang = localStorage.getItem('pr_lang') || 'ru';
if (!LANGS[lang]) lang = 'ru';
let T = LANGS[lang];

function setLang(l) {
  if (!LANGS[l]) return;
  lang = l;
  T = LANGS[lang];
  localStorage.setItem('pr_lang', lang);
  applyTranslations();
}

function applyTranslations() {
  // Update data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    if (typeof T[key] === 'string') el.textContent = T[key];
  });
  // Update data-i18n-html elements (allow HTML)
  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.dataset.i18nHtml;
    if (typeof T[key] === 'string') el.innerHTML = T[key];
  });
  // Update placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach((el) => {
    const key = el.dataset.i18nPh;
    if (typeof T[key] === 'string') el.placeholder = T[key];
  });
  // Update select options
  document.querySelectorAll('[data-i18n-opt]').forEach((el) => {
    const key = el.dataset.i18nOpt;
    if (typeof T[key] === 'string') el.textContent = T[key];
  });
  // Highlight active lang button
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  // Re-render current view
  if (typeof buildFilters === 'function') buildFilters();
  if (typeof render === 'function' && typeof currentView !== 'undefined' && currentView === 'list')
    render();
  if (
    typeof renderSettings === 'function' &&
    typeof currentView !== 'undefined' &&
    currentView === 'settings'
  )
    renderSettings();
}

document.addEventListener('DOMContentLoaded', () => applyTranslations());
